import java.util.*;

public class Boj_10950 {
    /**
     * A+B - 3 (B5)
     */
    public static void main(String[] args) {
      Scanner sc = new Scanner(System.in);
      
      int T = sc.nextInt();
      
      for(int t = 1; t <= T; t++) {
        
        int A = sc.nextInt();
        int B = sc.nextInt();
        
        System.out.println(A + B);
      }
      
    }
}