import java.util.*;

public class Boj_10869 {
    /**
     * 사칙연산 (B5)
     */
    public static void main(String[] args) {
      Scanner sc = new Scanner(System.in);
      
      int A = sc.nextInt();
      int B = sc.nextInt();
      
      System.out.println(A + B);
      System.out.println(A - B);
      System.out.println(A * B);
      System.out.println(A / B);
      System.out.println(A % B);
    }
}